--
-- Name: routes; Type: VIEW; Schema: POIs; Owner: chobeat
--

CREATE VIEW routes AS
 SELECT b.userid,
    b.day,
    min(b.arrival_time) AS start,
    max(b.arrival_time) AS "end",
    public.st_makeline((b.arrival_location)::public.geometry) AS route
   FROM ( SELECT steps.userid,
            steps.arrival_time,
            steps.arrival_location,
            steps."from",
            steps."to",
            date_trunc('day'::text, steps.arrival_time) AS day
           FROM steps
          ORDER BY steps.arrival_time) b
  GROUP BY b.userid, b.day
 HAVING (count(*) > 1)
  ORDER BY min(b.arrival_time);


